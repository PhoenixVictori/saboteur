public class Company{
	private float plumbing;
	private float power;
	private float airConditioning;
	private float reputation;
	private float budget;
	
	public void setPlumbing(float newPlumbing){
		this.plumbing = newPlumbing;
	}
	public void setPower(float newPower){
		this.power = newPower;
	}
	public void setAirConditioning(float newAirConditioning){
		this.airConditioning = newAirConditioning;
	}
	public void setReputation(float newReputation){
		this.reputation = newReputation;
	}
	public void setBudget(float newBudget){
		this.budget = newBudget;
	}
}